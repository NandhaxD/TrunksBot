import telebot
import requests
from telebot.types import InlineKeyboardMarkup, InlineKeyboardButton
from NandhaBot import BOT_TOKEN
bot = telebot.TeleBot(BOT_TOKEN)



bot.message_handler(commands=['logo'])
def logo(message):
    if len(message.command) <2:
         return bot.reply_to(message, "Give Name For Generate Logo.")
    logo_name = message.text.split(None, 1)[1]
    
    try:
       API = f"https://api.sdbots.tk/anime-logo?name={logo_name}"
       req = requests.get(API).url
       bot.send_photo(
            message.chat.id,
            photo=req,
            caption="Successfully Generated by Trunks",
            reply_to_message_id=message.id)
    except Exception as e:
        bot.reply_to(message, e)
  
bot.infinity_polling()     
